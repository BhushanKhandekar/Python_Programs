# SWAP USING 3RD VARIABLE 
n1 = int(input("enter num1 = "))            # num1 = n1 = 3
n2 = int(input("enter num2 = "))            # num2 = n2 = 5
temp = n1                                   # temp = 3
n1 = n2                                     # n1 = 5
n2 = temp                                   # n2 + 3
print("----after swapping----\n num1 =",n1," &  num2 =",n2)